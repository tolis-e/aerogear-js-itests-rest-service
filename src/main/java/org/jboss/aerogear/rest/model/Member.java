package org.jboss.aerogear.rest.model;

public class Member {

    private String id;

    private String description;

    public Member() {
    }

    public Member(String description) {
        this.description = description;
    }

    public Member(String id, String description) {
        this.id = id;
        this.description = description;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    @Override
    public String toString() {
        return "Data{ id=" + id + ", description=" + description + '}';
    }

}
